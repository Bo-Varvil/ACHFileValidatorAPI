package com.groupg.achfilevalidator.models;

public class EntryDetailAddenda {
	public String recordCode;
	public String addendaCode;
	public String paymentInfo;
	public String addendaSequenceNum;
	public String entrySequenceNum;
	
	public String getRecordCode() {
		return recordCode;
	}
	public void setRecordCode(String recordCode) {
		this.recordCode = recordCode;
	}
	public String getAddendaCode() {
		return addendaCode;
	}
	public void setAddendaCode(String addendaCode) {
		this.addendaCode = addendaCode;
	}
	public String getPaymentInfo() {
		return paymentInfo;
	}
	public void setPaymentInfo(String paymentInfo) {
		this.paymentInfo = paymentInfo;
	}
	public String getAddendaSequenceNum() {
		return addendaSequenceNum;
	}
	public void setAddendaSequenceNum(String addendaSequenceNum) {
		this.addendaSequenceNum = addendaSequenceNum;
	}
	public String getEntrySequenceNum() {
		return entrySequenceNum;
	}
	public void setEntrySequenceNum(String entrySequenceNum) {
		this.entrySequenceNum = entrySequenceNum;
	}
}
